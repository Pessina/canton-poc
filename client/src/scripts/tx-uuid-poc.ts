/**
 * PoC: Canton transaction_uuid / preparedTransactionHash correlation.
 *
 * Tests whether the user can get a unique identifier BEFORE tx execution
 * that the MPC can later observe in the committed transaction logs.
 *
 * Flow:
 *   1. Setup: sandbox, parties, VaultOrchestrator
 *   2. User: prepare a RequestEvmDeposit exercise → get preparedTransactionHash + transaction_uuid
 *   3. User: execute the prepared transaction → submit to chain
 *   4. MPC:  observe the update stream → check for preparedTransactionHash / transaction_uuid
 *   5. Verify: user-provided value == MPC-observed value
 *
 * Run:
 *   pnpm daml:sandbox  (in another terminal)
 *   pnpm tx-uuid-poc
 */

