/**
 * Extract all UUIDs from a base64-encoded PreparedTransaction protobuf.
 * The protobuf contains:
 *   - commandId (in SubmitterInfo) — the one the user sent
 *   - transaction_uuid (in Metadata) — generated by the prepare endpoint
 * Without a protobuf parser, we search for UUID-shaped strings in the decoded bytes.
 */
function extractAllUuids(preparedTransactionBase64: string): string[] {
  const buf = Buffer.from(preparedTransactionBase64, "base64");
  const text = buf.toString("utf8");

  const uuidPattern = /[0-9a-f]{8}-[0-9a-f]{4}-[0-9a-f]{4}-[0-9a-f]{4}-[0-9a-f]{12}/gi;
  return [...new Set(text.match(uuidPattern) ?? [])];
}

/**
 * Dump readable strings from a base64-encoded protobuf for inspection.
 */
function dumpReadableStrings(base64: string): string[] {
  const buf = Buffer.from(base64, "base64");
  const readable: string[] = [];
  let current = "";
  for (const byte of buf) {
    if (byte >= 0x20 && byte < 0x7f) {
      current += String.fromCharCode(byte);
    } else {
      if (current.length >= 4) readable.push(current);
      current = "";
    }
  }
  if (current.length >= 4) readable.push(current);
  return readable;
}

